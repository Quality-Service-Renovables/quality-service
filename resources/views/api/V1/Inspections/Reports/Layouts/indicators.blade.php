<h3 id="section5">Apéndice</h3>
<hr><br>
<table class="inspection-table">
    <tbody>
        <tr>
            <td style="background-color: #9c0; text-align: center"><strong>Normal</strong></td>
            <td>
                <span>
                    Componente o elemento en buen estado, apto para seguir funcionando de manera normal y eficiente.
                    En ocasiones puede observarse un desgaste propio del funcionamiento normal de la máquina.
                </span>
            </td>
        </tr>
        <tr>
            <td style="background-color: #fffd99; text-align: center"><strong>Leve</strong></td>
            <td>
                <span>
                    Componente o elemento en el que se han apreciado daños o defectos leves. No impide el funcionamiento
                    normal del aerogenerador, pero puede evolucionar de forma negativa. Debe realizarse un seguimiento de
                    la máquina de forma que se inspeccione nuevamente en un plazo no superior a 1 año.
                </span>
            </td>
        </tr>
        <tr>
            <td style="background-color: #fc9; text-align: center"><strong>Moderado</strong></td>
            <td>
                <span>
                    Componente o elemento en el que ya se aprecian daños o defectos desarrollados.
                    Estos defectos evolucionarán con seguridad a una categoría superior y pueden poner en peligro a medio
                    plazo la integridad de la máquina. Sin embargo, esto no impide el funcionamiento eficaz de la máquina.
                    Debe realizarse un seguimiento de la máquina de forma que se inspeccione nuevamente en un plazo no
                    superior a 6 meses.
                </span>
            </td>
        </tr>
        <tr>
            <td style="background-color: #ff9901; text-align: center"><strong>Grave</strong></td>
            <td>
                <span>
                    Componente o elemento en el que se aprecian con claridad daños o defectos plenamente desarrollados.
                    La máquina puede seguir funcionando, pero ello ocasionará un agravamiento muy rápido de los defectos
                    presentes. Debe realizarse una vigilancia estrecha de la máquina de forma que se inspeccione con una
                    periodicidad mensual para comprobar su evolución y poder anticiparse al fallo del componente.
                </span>
            </td>
        </tr>
        <tr>
            <td style="background-color: #ff0001; text-align: center"><strong>Peligro</strong></td>
            <td>
                <span>
                    Existen daños severos en algún componente de la máquina que impiden o afectan gravemente a su buen
                    funcionamiento. De seguir en funcionamiento, con seguridad se producirá un fallo catastrófico que
                    puede dañar uno o más componentes, ocasionar daños a las personas o al medioambiente. Debe ser reparado
                    de forma inmediata.
                </span>
            </td>
        </tr>
    </tbody>
</table>
